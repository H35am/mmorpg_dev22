    private void cb_charactersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cb_charactersMouseClicked
        // TODO add your handling code here:
        if(loggedInUser == null)
            return;
        
        Helper help = new Helper();
        Collection<Characters> characternames = help.getAllCharactersByUsername(loggedInUser.getUserName());
        
        for(Characters characterName : characternames)
        cb_characters.addItem(characterName);
    }//GEN-LAST:event_cb_charactersMouseClicked

