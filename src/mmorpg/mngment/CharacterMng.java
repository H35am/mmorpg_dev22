    private void btn_createCharacterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_createCharacterMouseClicked
        // TODO add your handling code here:
        txt_name.setText("");
        if(loggedInUser == null)
        {
            lbl_createCharError.setText("No logged in user found");
            return;
        }
        
        if("".equals(txt_name.getText()))
        {
            lbl_createCharError.setText("Fill in all the forms please");
            return;
        }
        
        Helper help = new Helper();
        Characters character = help.ReadCharacter(txt_name.getText());

        if(character != null)
        {
            lbl_createCharError.setText("Name allready exists");
            return;
        }
        
        int slots = loggedInUser.getCharacterSlots();
        
        if(slots <= 0)
        {
            lbl_createCharError.setText("Not enough slots for a new character");
            return;
        }
        
        String race = String.valueOf(cb_race.getSelectedItem());
        String className = String.valueOf(cb_class.getSelectedItem());
        Random rand = new Random();

        int level = rand.nextInt(100) + 1;
        
        Characters newChar = new Characters(txt_name.getText(), className, race, level);
        help.CreateCharacter(newChar, loggedInUser.getUserName());
        help.removeCharSlot(loggedInUser);
        
        setLoggedInUser(help.ReadUsers(loggedInUser.getUserName()));
        lbl_createCharError.setText("Succes!");
        
    }//GEN-LAST:event_btn_createCharacterMouseClicked

    private void btn_getcharacterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_getcharacterActionPerformed
        // TODO add your handling code here:
        if(loggedInUser == null)
            return;
        
        String selectedValue = String.valueOf(cb_characters.getSelectedItem());
        
        Helper help = new Helper();
        Characters character = help.ReadCharacter(selectedValue);
        
        lbl_name.setText(character.getName());
        lbl_class.setText(character.getClass1());
        lbl_race.setText(character.getRace());
        lbl_level.setText(String.valueOf(character.getLevel()));
        
        
    }//GEN-LAST:event_btn_getcharacterActionPerformed

